# Formatting strings
name = 'Stas'
age = 32
f_string = f"My name is {name}, i am {age} years"
print(f_string)
place_holders_string = 'My name is %s, i am %d years' % (name, age)
print(place_holders_string)
format_string = "My name is {0}, i am {1} years".format(name, age)
print(format_string)

